const isValidSudoku = (board) => {
  const rowSet = new Set();
  const columnSet = new Set();
  const boxSet = new Set();

  for (let i = 0; i < board.length; ++i) {
    const row = board[i];

    for (let j = 0; j < row.length; ++j) {
      const rowNumber = row[j];
      const columnNumber = board[j][i];
      const boxCharacter =
        board[3 * Math.floor(i / 3) + Math.floor(j / 3)][
          ((i * 3) % 9) + (j % 3)
        ];

      if (rowNumber !== ".") {
        if (columnSet.has(rowNumber)) {
          return false;
        }
        columnSet.add(rowNumber);
      }

      if (columnNumber !== ".") {
        if (rowSet.has(columnNumber)) {
          return false;
        }
        rowSet.add(columnNumber);
      }

      if (boxCharacter !== ".") {
        if (boxSet.has(boxCharacter)) {
          return false;
        }
        boxSet.add(boxCharacter);
      }
    }

    rowSet.clear();
    columnSet.clear();
    boxSet.clear();
  }

  return true;
};

